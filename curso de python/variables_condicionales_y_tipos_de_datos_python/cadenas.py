# Ejemplos de cadenas en Python las cadenas son inmutables 

name = 'CARLA Marcela' # cadena de caracteres
last_name = '   Florida     Roman  ' # cadena de caracteres con espacios al inicio y al final
print(5 * name) # Multiplicación de cadenas y mostrara 5 veces el nombre
print(name + ' ' + last_name) # Concatenación de cadenas por medio de un espacio
print(len(name))         # Longitud de la cadena
print(len(last_name))   # Longitud de la cadena
print(name.lower())     # Convierte la cadena a minúsculas
print(name.upper())     # Convierte la cadena a mayúsculas
print(last_name.strip())# Elimina los espacios en blanco al inicio y al final